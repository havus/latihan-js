var angkotKerja = 6
var jumlahAngkot = 10
var noAngkot = 1

for (noAngkot; noAngkot <= jumlahAngkot; noAngkot++)
    if (noAngkot <= angkotKerja) {
        console.log('Angkot No. ' + noAngkot + ' beroperasi dengan baik.')
} else {
        console.log('Angkot No. ' + noAngkot + ' tidak beroperasi dengan baik.')
}